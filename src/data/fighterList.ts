export function getFighter(fighterId) {
    const filtered = FIGHTER_LIST.filter(f => f.id === fighterId);
    if (filtered.length > 0) {
        return Object.freeze(filtered[0]);
    } else {
        return null;
    }
}

export function getWeightClass(weight) {
    if (weight < 70) {
        return "Balloon";
    } else if (weight < 80) {
        return "Feather";
    } else if (weight < 90) {
        return "Light";
    } else if (weight < 104) {
        return "Medium";
    } else if (weight < 110) {
        return "Heavy";
    } else if (weight < 118) {
        return "Superheavy";
    } else {
        return "Ultraheavy";
    }
}